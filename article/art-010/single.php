      <h2>Concept</h2>

      <p class="font_14 mt_40">
        HOLOTIC(ホロティック)は、映像と組み合わせて擬似的にホログラム映像を表現することができます。<br>
        このホログラム映像を通して将来のインターフェイスを表現してみました。<br>
        将来SFのような2次元から3次元になった場合どのようなインターフェイスになるのか<br>
        音とインターフェイスを融合したインタラクティムービーになります。
      </p>

      <h2>Concept color</h2>

      <p class="font_14 mt_40">
        HOLOTICは、コンセプトカラーとして「ブラック×オレンジ」を選びました。<br><br>

        「黒」は「都会的、クール、先進的」なイメージを持つと言われます。<br>
        「オレンジ」は「情熱、躍動感」な活動的でアグレッシブなイメージを持ち、対極とも言える要素を持つ2つのカラーを組み合わせることで、コントラストの強さや認識率の高さを演出します。<br>
        「テクノロジーとデザインとの融合」を元にハイブリッドを目指しているカラーになります。
      </p>

